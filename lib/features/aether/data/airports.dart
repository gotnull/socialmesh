import 'dart:math' as math;

/// An airport with IATA/ICAO codes, name, city, country, and coordinates.
class Airport {
  final String iata;
  final String icao;
  final String name;
  final String city;
  final String country;
  final double latitude;
  final double longitude;

  const Airport({
    required this.iata,
    required this.icao,
    required this.name,
    required this.city,
    required this.country,
    required this.latitude,
    required this.longitude,
  });

  /// Display label for the picker: "LAX - Los Angeles (KLAX)"
  String get displayLabel => '$iata - $city ($icao)';

  /// Compact label: "LAX - Los Angeles"
  String get compactLabel => '$iata - $city';

  /// Search across all fields (case-insensitive).
  bool matches(String query) {
    final q = query.toLowerCase();
    return iata.toLowerCase().contains(q) ||
        icao.toLowerCase().contains(q) ||
        name.toLowerCase().contains(q) ||
        city.toLowerCase().contains(q) ||
        country.toLowerCase().contains(q);
  }

  /// Great-circle distance to another airport in kilometres (haversine).
  double distanceToKm(Airport other) {
    const earthRadiusKm = 6371.0;
    final dLat = _toRadians(other.latitude - latitude);
    final dLon = _toRadians(other.longitude - longitude);
    final a =
        math.sin(dLat / 2) * math.sin(dLat / 2) +
        math.cos(_toRadians(latitude)) *
            math.cos(_toRadians(other.latitude)) *
            math.sin(dLon / 2) *
            math.sin(dLon / 2);
    final c = 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a));
    return earthRadiusKm * c;
  }

  static double _toRadians(double degrees) => degrees * math.pi / 180;
}

