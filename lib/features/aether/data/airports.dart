/// An airport with IATA and ICAO codes, name, and city.
class Airport {
  final String iata;
  final String icao;
  final String name;
  final String city;
  final String country;

  const Airport({
    required this.iata,
    required this.icao,
    required this.name,
    required this.city,
    required this.country,
  });

  /// Display label for the picker: "LAX - Los Angeles (KLAX)"
  String get displayLabel => '$iata - $city ($icao)';

  /// Compact label: "LAX - Los Angeles"
  String get compactLabel => '$iata - $city';

  /// Search across all fields (case-insensitive).
  bool matches(String query) {
    final q = query.toLowerCase();
    return iata.toLowerCase().contains(q) ||
        icao.toLowerCase().contains(q) ||
        name.toLowerCase().contains(q) ||
        city.toLowerCase().contains(q) ||
        country.toLowerCase().contains(q);
  }
}

